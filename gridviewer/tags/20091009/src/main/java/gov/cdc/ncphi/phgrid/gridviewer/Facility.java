package gov.cdc.ncphi.phgrid.gridviewer;

public class Facility {
	String facilityClassifier;
	String facilityName;
	public String getFacilityClassifier() {
		return facilityClassifier;
	}
	public void setFacilityClassifier(String facilityClassifier) {
		this.facilityClassifier = facilityClassifier;
	}
	public String getFacilityName() {
		return facilityName;
	}
	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}
}
